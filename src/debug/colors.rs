pub fn green() -> &'static str {
    return "\x1B[32m";
}

pub fn red() -> &'static str {
    return "\x1B[31m";
}

pub fn cyan() -> &'static str {
    return "\x1B[36m";
}

pub fn yellow() -> &'static str {
    return "\x1B[33m";
}

pub fn magenta() -> &'static str {
    return "\x1B[35m";
}

pub fn none() -> &'static str {
    return "\x1B[0m";
}
